export function effectivePrice(firm: PropFirm): number {
  return firm.discountedPrice ?? firm.regularPrice;
}

export function isActivationFree(firm: PropFirm): boolean {
  return firm.activationFeeUsd === null || firm.activationFeeUsd === 0;
}

/** Prix affichÃ© colonne Â« discounted Â» pour le calcul du total (fallback : regular). */
export function discountedOrRegularUsd(firm: PropFirm): number {
  return firm.discountedPrice ?? firm.regularPrice;
}

/** Total dÃ©marrage : prix discounted (ou regular si pas de promo) + frais dâ€™activation. */
export function evalStartupTotalUsd(firm: PropFirm): number {
  return (
    discountedOrRegularUsd(firm) +
    (isActivationFree(firm) ? 0 : Number(firm.activationFeeUsd))
  );
}

export function formatUsdCompact(value: number): string {
  const fixed = value % 1 === 0 ? value.toFixed(0) : value.toFixed(2);
  return `$${fixed}`;
}

/** Whole USD with grouping, e.g. 1000 â†’ "$1,000". */
export function formatUsdWholeGrouped(value: number): string {
  return new Intl.NumberFormat("en-US", {
    style: "currency",
    currency: "USD",
    maximumFractionDigits: 0,
  }).format(value);
}

/** Select Daily/Flex — lignes compare réservées au journal funded (éval unifiée « Tradeify Select »). */
export function isTradeifySelectVariantCompareRow(firm: PropFirm): boolean {
  if (firm.name !== "Tradeify") return false;
  return (
    firm.accountName === "Tradeify Select Daily" ||
    firm.accountName === "Tradeify Select Flex"
  );
}
